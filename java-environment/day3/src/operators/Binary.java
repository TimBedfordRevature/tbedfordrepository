package operators;

public class Binary {
	
	public static void main(String[] args) {
		
		int mod = 10 % 3; // prints remainder after division 
		System.out.println(mod);
		/* Any number % 2 will return 1 or 0 
		 * depending on whether the number is odd or even
		 */
		System.out.println("Hello There: " + 5 + 2);
		System.out.println(5 + 2 + " Hello There: " + 5 + 2);
		System.out.println("Hello There: " + (5+2));
		System.out.println("Hello There: " + (5>2));
	}

}
